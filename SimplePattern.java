public class SimplePattern {
    public static void main(String [] args){
        //we need two for loop each loop denote row and columns
        for(int i=1;i<=5;i++) {//denote row value
            for (int j = 1; j <= 5; j++) {//it's denote column value
                System.out.print("*");//print the *
            }
            System.out.println();//goto next line
        }
    }
}
